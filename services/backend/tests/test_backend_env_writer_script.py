def test_writer_output_can_be_loaded_as_settings(
    script_repo: Path,
    monkeypatch: pytest.MonkeyPatch,
) -> None:
    result = run_writer(
        script_repo,
        "--meshy-key",
        "meshy-from-flags",
        "--openai-key",
        "sk-openai-flags",
    )

    assert result.returncode == 0
    monkeypatch.chdir(script_repo / "services/backend")
    raw_values = dotenv_values(".env")
    settings = load_settings(
        env={key: value or "" for key, value in raw_values.items()}
    )
    assert settings.three_d_provider == "meshy"
    assert settings.meshy_api_key == "meshy-from-flags"
    assert settings.npc_provider == "openai"
    assert settings.openai_api_key == "sk-openai-flags"
    assert settings.print_provider == "local"


def run_writer(
    root: Path,
    *args: str,
    env: dict[str, str] | None = None,
) -> subprocess.CompletedProcess[str]:
    process_env = {
        "PATH": os.environ.get("PATH", ""),
        "HOME": os.environ.get("HOME", ""),
    }
    if env is not None:
        process_env.update(env)
    return subprocess.run(
        ["sh", "services/backend/scripts/write_backend_env.sh", *args],
        cwd=root,
        check=False,
        capture_output=True,
        text=True,
        env=process_env,
    )


def backend_env_path(root: Path) -> Path:
    return root / "services/backend/.env"
