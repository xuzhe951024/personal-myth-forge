@pytest.fixture
def script_repo(tmp_path: Path) -> Path:
    root = tmp_path / "repo"
    backend_root = root / "services/backend"
    scripts_root = backend_root / "scripts"
    source_root = Path(__file__).resolve().parents[3]
    source_scripts = source_root / "services/backend/scripts"
    if source_scripts.exists():
        shutil.copytree(source_scripts, scripts_root)
    else:
        scripts_root.mkdir(parents=True)
    subprocess.run(["git", "init"], cwd=root, check=True, capture_output=True, text=True)
    return root


def test_writer_blocks_missing_meshy_key(script_repo: Path) -> None:
    result = run_writer(script_repo, env={"OPENAI_API_KEY": "sk-openai-test"})

    assert result.returncode == 2
    assert "Missing MESHY_API_KEY" in result.stderr
    assert not backend_env_path(script_repo).exists()
    assert "sk-openai-test" not in result.stderr


