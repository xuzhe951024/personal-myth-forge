def test_writer_refuses_tracked_backend_env(script_repo: Path) -> None:
    backend_env_path(script_repo).write_text("MESHY_API_KEY=old\n", encoding="utf-8")
    subprocess.run(
        ["git", "add", "services/backend/.env"],
        cwd=script_repo,
        check=True,
        capture_output=True,
        text=True,
    )

    result = run_writer(
        script_repo,
        env={"MESHY_API_KEY": "meshy-secret-test", "OPENAI_API_KEY": "sk-openai-test"},
    )

    assert result.returncode == 1
    assert "services/backend/.env must stay untracked" in result.stderr
    assert backend_env_path(script_repo).read_text(encoding="utf-8") == "MESHY_API_KEY=old\n"


def test_writer_uses_environment_values_and_redacts_output(script_repo: Path) -> None:
    result = run_writer(
        script_repo,
        env={
            "MESHY_API_KEY": "meshy-secret-test",
            "OPENAI_API_KEY": "sk-openai-test",
            "TREATSTOCK_API_KEY": "treatstock-optional",
        },
    )

    assert result.returncode == 0
    assert "services/backend/.env written." in result.stdout
    assert "MESHY_API_KEY: configured (redacted)" in result.stdout
    assert "OPENAI_API_KEY: configured (redacted)" in result.stdout
    assert "meshy-secret-test" not in result.stdout + result.stderr
    assert "sk-openai-test" not in result.stdout + result.stderr
    assert "treatstock-optional" not in result.stdout + result.stderr
    assert backend_env_path(script_repo).read_text(encoding="utf-8") == EXPECTED_ENV_WITH_OPTIONAL


def test_writer_flags_override_environment(script_repo: Path) -> None:
    result = run_writer(
        script_repo,
        "--meshy-key",
        "meshy-from-flags",
        "--openai-key",
        "sk-openai-flags",
        "--openai-base-url",
        "https://api.openai.test/v1",
        "--print-provider",
        "treatstock",
        "--treatstock-key",
        "treatstock-from-flags",
        "--treatstock-base-url",
        "https://treatstock.test",
        env={"MESHY_API_KEY": "meshy-from-env", "OPENAI_API_KEY": "sk-openai-env"},
    )

    assert result.returncode == 0
    written = backend_env_path(script_repo).read_text(encoding="utf-8")
    assert "MESHY_API_KEY=meshy-from-flags" in written
    assert "OPENAI_API_KEY=sk-openai-flags" in written
    assert "OPENAI_API_BASE_URL=https://api.openai.test/v1" in written
    assert "PRINT_PROVIDER=treatstock" in written
    assert "TREATSTOCK_API_KEY=treatstock-from-flags" in written
    assert "TREATSTOCK_API_BASE_URL=https://treatstock.test" in written
    assert "meshy-from-env" not in written
    assert "sk-openai-env" not in written


