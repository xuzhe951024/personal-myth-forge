    local_config_path(script_repo).write_text("DEVELOPMENT_TEAM = OLD\n", encoding="utf-8")
    subprocess.run(
        ["git", "add", "apps/mobile/ios/Config/Deployment.local.xcconfig"],
        cwd=script_repo,
        check=True,
        capture_output=True,
        text=True,
    )

    result = run_writer(
        script_repo,
        env={
            "DEVELOPMENT_TEAM": "ABCDE12345",
            "PRODUCT_BUNDLE_IDENTIFIER": "com.example.personalmythforge",
            "PMF_BACKEND_BASE_URL": "http://192.168.1.10:8080",
        },
    )

    assert result.returncode == 1
    assert "must stay untracked" in result.stderr
    assert local_config_path(script_repo).read_text(encoding="utf-8") == "DEVELOPMENT_TEAM = OLD\n"


