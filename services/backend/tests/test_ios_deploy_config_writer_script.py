def test_writer_output_passes_existing_deploy_preflight(script_repo: Path) -> None:
    with health_server('{"status":"ok"}') as backend_url:
        writer_result = run_writer(
            script_repo,
            "--team",
            "ABCDE12345",
            "--bundle-id",
            "com.example.personalmythforge",
            "--backend-url",
            backend_url,
        )
        preflight_result = run_preflight(script_repo)

    assert writer_result.returncode == 0
    assert preflight_result.returncode == 0
    assert "Backend health: ok" in preflight_result.stdout


def run_writer(
    root: Path,
    *args: str,
    env: dict[str, str] | None = None,
) -> subprocess.CompletedProcess[str]:
    process_env = os.environ.copy()
    if env is not None:
        process_env.update(env)
    return subprocess.run(
        ["sh", "apps/mobile/ios/scripts/write_deploy_local_config.sh", *args],
        cwd=root,
        check=False,
        capture_output=True,
        text=True,
        env=process_env,
    )


def run_preflight(root: Path) -> subprocess.CompletedProcess[str]:
    return subprocess.run(
        ["apps/mobile/ios/scripts/deploy_preflight.sh"],
        cwd=root,
        check=False,
        capture_output=True,
        text=True,
    )


def local_config_path(root: Path) -> Path:
    return root / "apps/mobile/ios/Config/Deployment.local.xcconfig"


@contextlib.contextmanager
def health_server(body: str) -> Iterator[str]:
    class Handler(http.server.BaseHTTPRequestHandler):
        def do_GET(self) -> None:  # noqa: N802
            if self.path != "/health":
                self.send_response(404)
                self.end_headers()
                return
            self.send_response(200)
            self.send_header("Content-Type", "application/json")
            self.end_headers()
            self.wfile.write(body.encode("utf-8"))

        def log_message(self, format: str, *args: object) -> None:
            return

    with socketserver.TCPServer(("127.0.0.1", 0), Handler) as server:
        port = server.server_address[1]
        thread = threading.Thread(target=server.serve_forever)
        thread.start()
        try:
            yield f"http://0.0.0.0:{port}"
        finally:
            server.shutdown()
            thread.join(timeout=5)
